import Foundation

// MARK: - 4pt Spacing Grid

public enum WPSpacing {
    /// 4pt — minimal gaps, icon padding.
    public static let xxs: CGFloat = 4
    /// 8pt — tight spacing, inline elements.
    public static let xs: CGFloat = 8
    /// 12pt — compact padding, list row insets.
    public static let sm: CGFloat = 12
    /// 16pt — standard padding, content margins.
    public static let md: CGFloat = 16
    /// 24pt — section spacing, generous gaps.
    public static let lg: CGFloat = 24
    /// 32pt — group separation, modal padding.
    public static let xl: CGFloat = 32
    /// 48pt — screen-level spacing, empty state padding.
    public static let xxl: CGFloat = 48
}
